def _answer_str(value: Rational | bool | None) -> str:
    """Render the simulator's answer as the wire string the verifier parses.

    The Layer-3 simulator returns a SymPy ``Rational`` for a numeric item or a ``bool`` for
    a yes/no item (``None`` only on a no-answer EXPLAIN turn, which the ANSWER-only bot loop
    never hits). The turn loop's ``submitted_answer`` is the raw string the domain verifier
    parses: ``str(Rational(7, 12)) == "7/12"`` round-trips through ``_parse_to_rational``,
    and ``str(True) == "True"`` / ``str(False) == "False"`` round-trip through
    ``_parse_to_bool`` (case-insensitive 'true'/'false'). So the bot's verdict is identical
    to ``run_persona`` handing the verifier the value directly. ``None`` → ``""`` (wrong,
    never a crash).
    """
    return "" if value is None else str(value)


def _goal_kc_for_route(route_key: str) -> KnowledgeComponentId:
    """The goal KC a Turn-0 route launches into (the single-source-of-truth routing table)."""
    for option in routing_choices():
        if option.key == route_key:
            return option.routes_to
    raise ValueError(f"unknown route_key: {route_key!r}")


def _unit_slug_for_kc(kc: KnowledgeComponentId) -> str | None:
    """The catalog unit slug whose lessons teach ``kc``, or ``None`` if no unit covers it.

    Derived from the frozen curriculum catalog (``all_units``) so the assignment tracks the
    real unit/lesson structure rather than a hardcoded slug. ``CatalogLesson.kc_id`` is the
    plain KC string, so we compare against ``kc.value``.
    """
    for unit in all_units():
        for lesson in unit.lessons:
            if lesson.kc_id == kc.value:
                return unit.slug
    return None


def _already_seeded(session_factory: sessionmaker[OrmSession], session_id: str) -> bool:
    """Has this bot already been driven through the turn loop (idempotency guard)?

    True when a learner for ``session_id`` exists AND has persisted turns — meaning a prior
    ``run_student_bot`` already drove and recorded this bot's session. We then skip re-driving
    (which would append a duplicate Session + Turn history) and only re-assert the roster /
    assignment, which are themselves idempotent.
    """
    with session_factory() as db:
        learner = repo.get_learner(db, session_id)
        if learner is None:
            return False
        return bool(repo.load_turns_for_learner(db, learner.id))


def _drive_turns(profile: StudentBotProfile, session_factory: sessionmaker[OrmSession]) -> None:
    """Drive one persona through the REAL turn loop with persistence ON.

    Starts a session on the profile's route (pinned to ``profile.session_id`` so the problem
    walk is reproducible and the run is idempotent), then loops: read the live ``Problem`` the
    store is showing → ask the Layer-3 simulator for the persona's deterministic action →
    submit it through ``SessionStore.process_turn`` (which verifies, updates mastery, applies
    the policy, serves the next problem, and persists the Turn + MasteryState rows). Stops when
    the lesson completes (a mastering persona confirmed the KC), the store serves no next
    problem, or ``max_turns`` is reached (the cap that bounds a never-mastering persona).

    The bot store runs with NO predictor/voice/hint providers and the proactive arm OFF: bots
    are pure behavioral data, so the LLM/HelpNeed layers stay out (CLAUDE.md §8.1) and the run
    is deterministic.
    """
    store = SessionStore(session_factory=session_factory)
    started = store.start(profile.route_key, session_id=profile.session_id)
    surface_state = started.surface_state
    for _ in range(profile.max_turns):
        problem = store.current_problem(started.session_id)
        if problem is None:
            break
        action = simulate_action(profile.persona, problem)
        request = TurnRequest(
            session_id=started.session_id,
            problem_id=problem.problem_id,
            action=ActionType.SUBMIT_ANSWER,
            submitted_answer=_answer_str(action.submitted_answer),
            surface_state=surface_state,
            latency_ms=max(0, round(action.think_time_seconds * 1000)),
            hint_used=action.requested_hint,
        )
        response = store.process_turn(request)
        if response.lesson_complete or response.next_problem is None:
            break
        surface_state = response.next_surface_state


def _roster_and_assign(
    profile: StudentBotProfile,
    session_factory: sessionmaker[OrmSession],
    *,
    teacher_id: int,
    now: datetime,
) -> int:
    """Roster the bot's learner to the teacher (+ assign its unit best-effort); return its id.

    Rostering (``add_student_to_roster``) is what makes the bot appear on the teacher's
    dashboard. We also label the learner with a readable bot email so the dashboard's
    name (the email local-part, ``teacher_service._display_name``) reads e.g.
    ``"procedure-priya"`` instead of ``"Student N"``, and assign the unit that teaches the
    bot's goal KC as a steering hint. All three are idempotent (roster/assignment upsert on
    their unique keys), so re-running is safe. Unit assignment is best-effort: if the curriculum
    is unseeded and the unit row is absent, we skip it rather than fail the seed.
    """
    with session_factory() as db:
        learner = repo.get_or_create_learner(db, profile.session_id)
        db.flush()
        if not learner.email:
            learner.email = _bot_email(profile.display_name)
        repo.add_student_to_roster(db, teacher_id, learner.id)
        slug = _unit_slug_for_kc(_goal_kc_for_route(profile.route_key))
        if slug is not None:
            unit = repo.get_unit(db, slug)
            if unit is not None:
                repo.assign_unit(
                    db,
                    teacher_id=teacher_id,
                    student_id=learner.id,
                    unit_id=unit.id,
                    now=now,
                )
        db.commit()
        return learner.id


def _bot_email(display_name: str) -> str:
    """A readable, clearly-synthetic email for a bot, so the dashboard shows a persona label.

    The teacher dashboard derives a student's display name from the email local-part, so a
    slug of the persona name (e.g. ``"procedure-priya"``) renders a recognizable label while
    the ``@bots.whollymath.demo`` domain marks the row as a generated bot, not a real account.
    """
    slug = "-".join(display_name.lower().split())
    return f"{slug}@bots.whollymath.demo"


def run_student_bot(
    profile: StudentBotProfile,
    session_factory: sessionmaker[OrmSession],
    *,
    teacher_id: int,
    now: datetime,
) -> int:
    """Drive one persona bot through the REAL turn loop with persistence; return its learner id.

    Idempotent on ``profile.session_id``: the first run drives the turns (persisting genuine
    Session/Turn/MasteryState rows) and rosters the learner to ``teacher_id``; a re-run detects
    the already-seeded session, skips re-driving (so no duplicate turns), and only re-asserts
    the (idempotent) roster + assignment. Returns the bot's learner id either way.
    """
    if not _already_seeded(session_factory, profile.session_id):
        _drive_turns(profile, session_factory)
    return _roster_and_assign(profile, session_factory, teacher_id=teacher_id, now=now)


def seed_demo_class(
    session_factory: sessionmaker[OrmSession],
    *,
    now: datetime,
) -> list[int]:
    """Run every demo bot profile and roster them to the shared demo teacher; return their ids.

    Idempotent end-to-end: ``get_or_create_demo_teacher`` keys on a fixed session id (one
    teacher), and each ``run_student_bot`` maps to a stable learner — so re-running yields the
    same demo class (same teacher, same students, same turns), stable across reboots. Produces
    real numbers via the live turn loop, not hand-set rows.
    """
    with session_factory() as db:
        teacher = repo.get_or_create_demo_teacher(db)
        db.commit()
        teacher_id = teacher.id
    return [
        run_student_bot(profile, session_factory, teacher_id=teacher_id, now=now)
        for profile in DEMO_BOT_PROFILES
    ]


