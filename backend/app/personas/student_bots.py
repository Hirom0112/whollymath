"""Persona-driven student BOTS that generate real demo data through the live engine.

WHY (owner direction, 2026-05-30): instead of seeding hand-set demo rows, we want SOLID
data that looks like real students actually used the app — generated by driving the
existing persona behavioral simulators (``app.personas`` Layer 3 — Priya/Sam/Nate/Cleo/
Hugo) through the REAL turn loop, so every mastery/turn number comes from the real
verify→mastery→persist path. The teacher dashboard then reads genuine students, and the
whole plumbing is exercised end-to-end.

HONEST SCOPE (confirmed with the model lane, 2026-05-30): only the FIVE fraction-
foundation KCs are content-complete (``EQUIVALENCE / COMMON_DENOMINATOR / ADDITION_UNLIKE
/ SUBTRACTION_UNLIKE / NUMBER_LINE_PLACEMENT``), and the five personas only configure
knowledge for those. So the bots generate **fraction-lesson data only** — enough to
populate the dashboard and validate the real plumbing, but NOT weak-KC or non-fraction
data (that is gated on the Grade-6 content build). The bot framework generalizes for free
once that content + grade-6 persona configs exist; it simply has nothing to drive on the
other 41 KCs today.

DETERMINISTIC, NO LLM (CLAUDE.md §8.1/§8.3). The driving is pure code:
  - Layer 2 = each persona's hand-written knowledge state (KCs known, misconceptions, hint
    probability).
  - Layer 3 (``personas/simulator.simulate_action``) = a pure ``(persona, problem) →
    action`` function. Wrong answers come from the SymPy misconception generators, not
    invention.
  - The bot answers whatever problem the REAL scheduler serves (``SessionStore`` picks the
    KC/representation/difficulty), so the data reflects the real adaptive turn loop.
The LLM (Layer 4) is never reached here — bots produce every behavioral number with the
voice/hint/predictor providers off.

THE TWO HALVES
--------------
1. Data generation (THIS module, backend): ``run_student_bot`` drives one persona through
   ``SessionStore.process_turn`` with persistence ON → genuine Learner/Session/Turn/
   MasteryState rows; ``seed_demo_class`` runs every profile and rosters them to the demo
   teacher. Both idempotent on the stable per-bot ``session_id``.
2. Visual-QA sweep (CROSS-LANE, frontend/e2e): a Playwright pass that screenshots every
   screen on this real data. That harness is T3's lane (T1 must not touch ``frontend/``);
   this module owns only the data the sweep runs against.
"""

